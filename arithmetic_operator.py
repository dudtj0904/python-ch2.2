# 사칙연산
print(2 * 3)
print(2 + 3)
print(2 - 3)
print(2 / 3)
print(2 / 3.0)
print(2.0 / 3)
print(2.0 / 3.0)

# // : 몫 연산자
print(2 // 3)

# ** : 지수승
print(2 ** 3)

# % : 나머지 연산자
print(2 % 3)

# divmod()
print(divmod(2, 3))

# 연산자 우선 순위
print(2 + 3 * 4)
print(-2 + 3 * 4)
print(-(2 + 3) * 4)

print(4 / 2 *2)

# 결합 순서 주위
print(2 ** 3 ** 4)      # 2 ** 12
print((2 ** 3) ** 4)    # 8 ** 4
print(2 ** (3 ** 4))    # 2 ** 12



